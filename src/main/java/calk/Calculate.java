public class Calculate {
    public int calcAdd(int i, int i1) {
        return i+i1;
    }

    public int calkMult(int i, int i1) {
        return i*i1;
    }

    public int calcSubtrac(int i, int i1) {
        return i-i1;
    }


    public int calcdiv(int i, int i1) {
        return i/i1;
    }



}
